package jsonValue;

public class JsonValueUnsupportedOperationException extends UnsupportedOperationException {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 4075658063031302010L;

	public JsonValueUnsupportedOperationException() {
		super();
	}

	public JsonValueUnsupportedOperationException(String message) {
		super(message);
	}

	public JsonValueUnsupportedOperationException(Throwable cause) {
		super(cause);
	}

	public JsonValueUnsupportedOperationException(String message, Throwable cause) {
		super(message, cause);
	}

}
