package jsonValue;

public class JsonValueBuildException extends RuntimeException {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -7294512897813785687L;
	
	public JsonValueBuildException() {
		super();
	}

	public JsonValueBuildException(String message) {
		super(message);
	}

	public JsonValueBuildException(Throwable cause) {
		super(cause);
	}

	public JsonValueBuildException(String message, Throwable cause) {
		super(message, cause);
	}

}
